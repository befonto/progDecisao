
'''
Fazer um algoritmo que peça um número, e ao final, informe se o número digitado está acima de 1000 ou abaixo de 1000.
'''

num = int(input("Insira um número: "))

if ( num >= 1000 ):
    print(f"{num} é um número que está acima de 1000.")

else:
    print(f"{num} é um número que está abaixo de 1000.")
